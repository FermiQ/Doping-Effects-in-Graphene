c         IF (GAUGE .EQ. 'Y') THEN
c            GETTRANS = GETTRANSXY(TVALS, LIMX, LIMY, E, FLUX, WRAPX)
c         END IF
      ELSE
         IF (CURRENT .EQ. 'Y') THEN
            IF (GAUGE .EQ. 'X') THEN
               GETTRANS = GETTRANSYX(TVALS, LIMX, LIMY, E, FLUX, WRAPX)
            END IF
            IF (GAUGE .EQ. 'Y') THEN
               GETTRANS = GETTRANSYY(TVALS, LIMX, LIMY, E, FLUX, WRAPX)
            END IF
         END IF
      END IF

      RETURN
