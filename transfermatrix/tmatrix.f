     +                                   LIMY, E, FLUX, WRAPX)
      IMPLICIT NONE
      INTEGER LIMY, WRAPX, LIMX
      DOUBLE PRECISION TVALS(LIMX), E, FLUX
      CHARACTER CURRENT, GAUGE

c      DOUBLE PRECISION GETTRANSXX
